package org.example.javapk.OOPs;

public class Methods {

    //1) Approach : No param no return value
    void methods()
    {
        System.out.println("Hello....");
    }

    //2) Approach 2: No params returns value
    String methods1()
    {
        return "hello how are you";
    }
    //3) Takes param, No return a value
    void methods2(String name)
    {
        System.out.println("where are you...."+name);
    }

    //4) Takes param, returns a value
    String methods3(String name)
    {
        return ("Hello Beautiful "+name);
    }
}
